package C5Evaluacion;

public class ValorX_Math 
{
	public static void main(String[] args) 
	{
		System.out.printf (" x = Math.abs(7.5)\nEl valor de x es %.2f\n",Math.abs(7.5));
		System.out.printf ("\n x = Math.floor(7.5)\nEl valor de x es %.2f\n",Math.floor(7.5));
		System.out.printf ("\n x = Math.abs(0.0)\nEl valor de x es %.2f\n",Math.abs(0.0));
		System.out.printf ("\n x = Math.ceil (0.0)\nEl valor de x es %.2f\n",Math.ceil(0.0));
		System.out.printf ("\n x = Math.abs (-6.4)\nEl valor de x es %.2f\n", Math.abs (-6.4));
		System.out.printf ("\n x = Math.ceil (-6.4)\nEl valor de x es %.2f\n", Math.ceil (-6.4));
		System.out.printf ("\n x = Math.ceil(-Math.abs(-8 + Math.floor(-5.5)\nEl valor de x es: %.2f\n",Math.ceil(-Math.abs(-8 + Math.floor(-5.5))));
	}
}
